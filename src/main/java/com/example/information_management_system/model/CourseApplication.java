package com.example.information_management_system.model;

public class CourseApplication {
    private int id;
    private String teacherName;
    private String name;
    private String category;
    private double point;
    private String classroom;
    private int weekStart;
    private int weekEnd;
    private String period;
    private String time;
    private String college;
    private String term;
    private int classNum;
    private String type;
    private int capacity;
    private String status;
    private String intro;
    private String examination;
    private String f_reason;
    private boolean published;
    private double regularRatio;
    private double finalRatio;

    public int getId() { return id; }
    public void setId(int id) { this.id = id; }
    public String getTeacherName() { return teacherName; }
    public void setTeacherName(String teacherName) { this.teacherName = teacherName; }
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public String getCategory() { return category; }
    public void setCategory(String category) { this.category = category; }
    public double getPoint() { return point; }
    public void setPoint(double point) { this.point = point; }
    public String getClassroom() { return classroom; }
    public void setClassroom(String classroom) { this.classroom = classroom; }
    public int getWeekStart() { return weekStart; }
    public void setWeekStart(int weekStart) { this.weekStart = weekStart; }
    public int getWeekEnd() { return weekEnd; }
    public void setWeekEnd(int weekEnd) { this.weekEnd = weekEnd; }
    public String getPeriod() { return period; }
    public void setPeriod(String period) { this.period = period; }
    public String getTime() { return time; }
    public void setTime(String time) { this.time = time; }
    public String getCollege() { return college; }
    public void setCollege(String college) { this.college = college; }
    public String getTerm() { return term; }
    public void setTerm(String term) { this.term = term; }
    public int getClassNum() { return classNum; }
    public void setClassNum(int classNum) { this.classNum = classNum; }
    public String getType() { return type; }
    public void setType(String type) { this.type = type; }
    public int getCapacity() { return capacity; }
    public void setCapacity(int capacity) { this.capacity = capacity; }
    public String getStatus() { return status; }
    public void setStatus(String status) { this.status = status; }
    public String getIntro() { return intro; }
    public void setIntro(String intro) { this.intro = intro; }
    public String getExamination() { return examination; }
    public void setExamination(String examination) { this.examination = examination; }
    public String getF_reason() { return f_reason; }
    public void setF_reason(String f_reason) { this.f_reason = f_reason; }
    public boolean isPublished() { return published; }
    public void setPublished(boolean published) { this.published = published; }
    public double getRegularRatio() { return regularRatio; }
    public void setRegularRatio(double regularRatio) { this.regularRatio = regularRatio; }
    public double getFinalRatio() { return finalRatio; }
    public void setFinalRatio(double finalRatio) { this.finalRatio = finalRatio; }
}
